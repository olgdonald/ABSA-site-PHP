                        <div id="tab-content-1" class="container tab-pane active">
                            <p>L'Association Bouche des Sourds d’Afrique (ABSA) a été fondée en 2007 par de jeunes volontaires à Yaoundé, Cameroun, et officiellement enregistrée en 2010. Nous travaillons activement pour réduire l'exclusion et la marginalisation des personnes sourdes dans la société et pour les intégrer pleinement.</p>
                        </div>
                        <div id="tab-content-2" class="container tab-pane fade">
                            <p>Notre mission est de sensibiliser la population sur la situation des personnes sourdes et de lutter contre les préjugés tels que l'« incapacité », la « magie noire » et la « malédiction divine ». Nous visons à offrir un accès équitable à l'éducation, à l'emploi et aux soins de santé pour les personnes sourdes.</p>
                        </div>
                        <div id="tab-content-3" class="container tab-pane fade">
                            <p>Notre vision est de créer une société inclusive où les personnes sourdes peuvent vivre sans discrimination. Nous aspirons à développer des opportunités de formation et d'emploi pour les personnes sourdes, à construire et maintenir des écoles spécialisées, et à offrir des conseils et un accompagnement continus pour les sourds et leurs familles.</p>
                        </div>
